let currentLang = localStorage.getItem('lot_lang') || 'es';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('lot_lang', lang);
  document.documentElement.lang = lang;
  
  // Highlight switcher
  document.querySelectorAll('.lang-btn').forEach(b => {
    b.style.opacity = b.dataset.lang === lang ? '1' : '0.5';
  });

  // Update static nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (i18n[lang][key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = i18n[lang][key];
      } else {
        el.innerHTML = i18n[lang][key];
      }
    }
  });

  // Update labels (for optgroups)
  document.querySelectorAll('[data-i18n-label]').forEach(el => {
    const key = el.getAttribute('data-i18n-label');
    if (i18n[lang][key]) {
      el.label = i18n[lang][key];
    }
  });

  // Actualiza los titles flotantes (ej. tooltips de grid view)
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    if (i18n[lang][key]) {
      el.title = i18n[lang][key];
    }
  });

  // Refresh dynamic info
  if (typeof renderAll === 'function') {
    // Only re-render if categories exist
    if (document.getElementById('catsGrid')) {
      renderAll();
    }
  }
}

window.addEventListener('DOMContentLoaded', () => {
  setLang(currentLang);
});
